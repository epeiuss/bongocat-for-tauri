/**
 * 获取窗口在屏幕上可占用的宽度
 */
export const getAvailWidth = () => window.screen.availWidth;

/**
 * 获取窗口在屏幕上可占用的高度
 */
export const getAvailHeight = () => window.screen.availHeight;

/**
 * 获取屏幕的宽度
 */
export const getScreenWidth = () => window.screen.width;

/**
 * 获取屏幕的高度
 */
export const getScreenHeight = () => window.screen.height;

/**
 * 获取窗口的宽度
 */
export const getInnerWidth = () => window.innerWidth;

/**
 * 获取窗口的高度
 */
export const getInnerHeight = () => window.innerHeight;
